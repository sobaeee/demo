
public class Operator3 {

	public static void main(String[] args) {
		// 산술연산자 + - * / %(나머지값)
				int i = 10;
				int j = 3;
				
				System.out.println(i+j);
				System.out.println(i-j);
				System.out.println(i*j);
				System.out.println(i/j);
				System.out.println(i/j);
				System.out.println(i%j);

	}

}
